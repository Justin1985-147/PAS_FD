function[TAO,S,H,P,N,PS]=TWYS(T,SH)
% J2000系统幅角表达式,"天文元素"
global IHS DT FL
H0=180/pi;
% J2000系统幅角表达式
%《固体潮汐与引潮参数》、《精密引潮位展开及某些诠释》中为218.31643
%《新的引潮位完全展开》中为281.31643，结合《地层绝对应力测量与钻孔应变测量》一书P27，判断281应该为笔误
S=218.31643+(481267.88128-(0.00161-0.000005*T)*T)*T;%月亮平黄经
S=FX(S);
H=280.46607+(36000.76980+0.00030*T)*T;%太阳平黄经
H=FX(H);
P=83.35345+(4069.01388-(0.01031+0.00001*T)*T)*T;%月亮近地点平黄经
P=FX(P);
N=125.04452-(1934.13626-(0.00207+0.000002*T)*T)*T;%月亮升交点平黄经
N=FX(N);
PS=282.93835+(1.71946+(0.00046+0.000003*T)*T)*T;%太阳近地点平黄经
PS=FX(PS);
% 源自《精密引潮位展开及某些诠释》
DL=-0.00478*sin(N/H0)-0.00037*sin(2*H/H0);%黄经章动改正
%附加改正
DS=0.00396*sin((60.57-132.87*T)/H0)+0.00202*sin(N/H0);
%%%%%%%%%%%%%%
DH=0.00178*sin((251.39+20.20*T)/H0);
DH=DH+(1.866-0.016*T)/3600*sin((207.51+150.27*T)/H0);
DH=DH-0.00479*(T+0.003*T*T)*sin((H-PS)/H0);
DH=DH-0.00200*sin((67.20+32964.47*T)/H0);
DH=DH-0.00154*sin((16.85-45036.89*T)/H0);
DH=DH+0.00134*sin((81.51+22518.44*T)/H0);
DH=DH+0.00179*sin((S-H)/H0);
%%%%%%%%%%%%
DP=-0.00058*sin((71.40+20.20*T)/H0)-0.00058*sin(N/H0);
DN=0.02666*sin(N/H0)+0.00433*sin((N+272.75-2.30*T)/H0)+0.00052*sin((N+288.75-0.90*T)/H0);
S=S+DS+DL;
H=H+DH+DL;
P=P+DP+DL;
N=N+DN+DL;
PS=PS+DL;
T1=T-DT/3600/876600;
TAO=(SH-IHS+18.6973746+(2400.0513369+(0.000025862+0.0000000017222*T1)*T1)*T1)*15-S+FL;%地方平月亮时
TAO=FX(TAO);

    function y=FX(X)
        y=X-fix(X/360)*360;%去掉多余的360度周期
    end
end